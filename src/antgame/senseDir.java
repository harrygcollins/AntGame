package antgame;

/**
 * The enumerators representing the sense directions
 *
 * @author Team 13
 */
public enum senseDir {

    /**
     * Enum representing direction here
     */
    HERE,
    /**
     * Enum representing direction ahead
     */
    AHEAD,
    /**
     * Enum representing direction left-ahead
     */
    LEFTAHEAD,
    /**
     * Enum representing direction right-ahead
     */
    RIGHTAHEAD;
}
